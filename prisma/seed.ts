async function main() {
  console.log(`Seeding ${songs.length} songs...`);

  for (const song of songs) {
    await prisma.song.upsert({
      where: { slug: song.slug },
      update: {
        title: song.title,
        category: song.category,
        sections: {
          deleteMany: {},
          create: song.sections,
        },
      },
      create: {
        title: song.title,
        slug: song.slug,
        category: song.category,
        sections: { create: song.sections },
      },
    });
    console.log(`  ✓ ${song.title}`);
  }

  console.log("Done.");
}

main()
  .catch((err) => {
    console.error(err);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
